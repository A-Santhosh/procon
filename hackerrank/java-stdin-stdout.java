import java.util.*;

class Solution{
	public static void main(String[] args){
		Scanner cin=new Scanner(System.in);
		int a=cin.nextInt();
		double b=cin.nextDouble();
		cin.nextLine();
		String c=cin.nextLine();
		System.out.println("String: "+c);
		System.out.println("Double: "+String.valueOf(b));
		System.out.println("Int: "+String.valueOf(a));
	}
}