#!/usr/bin/ruby
a=DATA.map{|e|e.chomp.split('')}
expr=gets.chomp
(-5..5).each{|x|
	y=eval expr.gsub('x',x.to_s)
	if -5<=y&&y<=5
		a[-y+5][x+5]='*'
	end
}
puts a.map(&:join)

__END__
